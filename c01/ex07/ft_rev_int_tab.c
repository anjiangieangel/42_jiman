void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	add;

	i = 0;
	j = size -1;
	while (i < j)
	{
		add = tab[i];
		tab[i] = tab[j];
		tab[j] = add;
		i++;
		j--;
	}
}
